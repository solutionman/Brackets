public class Part {
    private int indFirst;
    private int indLast;
    private String value;

    public int getIndFirst() {
        return indFirst;
    }

    public void setIndFirst(int indFirst) {
        this.indFirst = indFirst;
    }

    public int getIndLast() {
        return indLast;
    }

    public void setIndLast(int indLast) {
        this.indLast = indLast;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
